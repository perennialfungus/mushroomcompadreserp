const regexTranslations: Array<[RegExp, (match: RegExpMatchArray) => string]> = [
  [/^Role dashboard: (.+)$/i, (match) => `Painel da funcao: ${match[1]}`],
  [/^Cached (.+) \/ (\d+)s TTL$/i, (match) => `Em cache ${match[1]} / TTL ${match[2]}s`],
  [/^(\d+) alerts$/i, (match) => `${match[1]} alertas`],
  [/^(\d+) methods$/i, (match) => `${match[1]} metodos`],
  [/^(\d+) critical$/i, (match) => `${match[1]} criticos`],
  [/^(\d+) packaging line\(s\)$/i, (match) => `${match[1]} linha(s) de embalagem`],
  [/^(.+) \/ (.+) \/ (.+)$/i, (match) => `${match[1]} / ${translateVisibleText(match[2] ?? "")} / ${translateVisibleText(match[3] ?? "")}`]
];

function preserveWhitespace(original: string, translated: string): string {
  const leading = original.match(/^\s*/)?.[0] ?? "";
  const trailing = original.match(/\s*$/)?.[0] ?? "";
  return `${leading}${translated}${trailing}`;
}

export function translateVisibleText(value: string): string {
  const trimmed = value.trim();
  if (!trimmed) {
    return value;
  }

  const exact = exactTranslations[trimmed];
  if (exact) {
    return preserveWhitespace(value, exact);
  }

  for (const [regex, replacer] of regexTranslations) {
    const match = trimmed.match(regex);
    if (match) {
      return preserveWhitespace(value, replacer(match));
    }
  }

  return value;
}

export function translatePortugueseUi(root: ParentNode = document): void {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "TEXTAREA"].includes(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }
      if (["INPUT", "OPTION"].includes(parent.tagName)) {
        return NodeFilter.FILTER_SKIP;
      }
      return node.nodeValue?.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_SKIP;
    }
  });

  const textNodes: Text[] = [];
  while (walker.nextNode()) {
    textNodes.push(walker.currentNode as Text);
  }
  textNodes.forEach((node) => {
    const next = translateVisibleText(node.nodeValue ?? "");
    if (next !== node.nodeValue) {
      node.nodeValue = next;
    }
  });

  root.querySelectorAll?.("input, textarea, [aria-label], [title], option").forEach((element) => {
    ["placeholder", "aria-label", "title"].forEach((attribute) => {
      const current = element.getAttribute(attribute);
      if (current) {
        const translated = translateVisibleText(current);
        if (translated !== current) {
          element.setAttribute(attribute, translated);
        }
      }
    });
    if (element.tagName === "OPTION" && element.textContent) {
      element.textContent = translateVisibleText(element.textContent);
    }
    if (
      (element instanceof HTMLInputElement || element instanceof HTMLTextAreaElement) &&
      document.activeElement !== element &&
      element.value
    ) {
      const translated = translateVisibleText(element.value);
      if (translated !== element.value) {
        element.value = translated;
      }
    }
  });
}
